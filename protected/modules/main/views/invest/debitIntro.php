		<div class="pa">
			<p>借款人质押担保、支付一定利息筹集贷款，投资人针对借款项目出资，获得利息收益。</p>
			<p><span>1.</span> 抵押担保贷款品种适用于需求者短期的资金周转；</p>
			<p><span>2.</span> 借款期限分1-12个月；</p>
			<p><span>3.</span> 借款金额根据借款者提供的抵押物价值而定，目前借贷宝支持的抵押物为域名、房产和车辆；</p>
			<p><span>4.</span> 借款利率限定在同期人民银行基准利率4倍以内。</p>
