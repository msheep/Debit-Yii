		<div class="caption"><span>了解好贷网</span></div>
		<p class="pa">民间借贷是指自然人之间、自然人与法人之间、自然人与其它组织之间借贷。只要双方当事人意见表示真实即可认定有效，因借贷产生的抵押相应有效，但利率不得超过人民银行规定的相关利率。民间借贷分为民间个人借贷活动和公民与金融企业之间的借贷。民间个人借贷活动必须严格遵守国家法律、行政法规的有关规定，遵循自愿互助、诚实信用原则。 </p>
		<p class="pa">民间借贷是《民法通则》中一种认可的民事法律行为，行为人在具有完全民事行为能力（即年满18周岁，且不存在足以影响自身行为的精神疾病的情形）、意思表示真实，借款合同符合法律、行政法规规定，则该借款合同完全受到《合同法》等法律的保护。民间借贷是民间资本的一种投资渠道，作为银行金融的有效补充已逐渐成为民间金融的一种重要形式。</p>
		<div class="caption"><span>选择好贷网投资的理由</span></div>
